export const RegisterSchema = {
  body: {
    type: "object",
    required: ["email", "password"],
    properties: {
      email: { type: "string" },
      password: { type: "string" }
    }
  }
};

export const LoginSchema = {
  body: {
    type: "object",
    required: ["email", "password"],
    properties: {
      email: { type: "string" },
      password: { type: "string" }
    }
  }
};


class AuthSchemas {
    static register = RegisterSchema;
    static LoginDto = LoginSchema;
}

export default AuthSchemas;
